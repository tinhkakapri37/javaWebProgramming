package data;

/**
 *
 * @author DELL 5440
 */
public class Student {
    private int id;
    private String name;
    private String major;
    private String dob;

    public Student() {
    }

    public Student(int id, String name, String major, String dob) {
        this.id= id;
        this.name = name;
        this.major = major;
        this.dob = dob;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    
    
}

